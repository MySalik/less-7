import React, {useState} from 'react';
import moment from 'moment'
import './index.css'


function DateTime(props) {
    return (
        <p className="date">{props.date}</p>
    )
}

function DateTimePretty(Component){
    return function (props){
        let now = moment(new Date());
        let duration = moment.duration(now.diff(props.date));
        let hours = duration.asHours()
        let date
        if(hours<1)
            date = `${duration.asMinutes().toFixed(0)} минут назад`
        else if (hours<24)
            date = `${hours.toFixed(0)} часов назад`
        else
            date = `${duration.asDays().toFixed(0)} дней назад`
        return <Component {...props} date={date}/>
    }
}

const DateNewFormat = DateTimePretty(DateTime)


function Video(props) {
    return (
        <div className="video">
            <iframe src={props.url} frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
            <DateNewFormat date={props.date} />
        </div>
    )
}

function VideoList(props) {
    return props.list.map(item => <Video url={item.url} date={item.date} />);
}

